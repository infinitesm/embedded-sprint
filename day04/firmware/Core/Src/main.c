void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim) {
	// Make sure we are getting this interrupt from TIM2 that we set up
	if (htim->Instance == TIM2) {
		// this interrupt triggers every 1 ms since the timer is set to 1 kHz, and we will toggle the LED every 100ms.
		// but we only want to do that if we enabled blinking through the user button.
		if (enable_blink) {
			// we want to blink the LED every 100ms, and the timer runs at 1 kHz.
			// that means that we receive an interrupt every 1ms, so we can simply count to 100 then toggle the LED.
			if (++ms >= 100) {
				// toggles the LED
				HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
				// reset our counter so we can continue toggling in the future.
				ms = 0;
			}
		}

	}
}

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin) {
	// every time the blue user button is pressed, we toggle the enable blinking status.
	if (GPIO_Pin == GPIO_PIN_13) {
		// since the user button is mechanical, it may bounce and trigger multiple interrupts
		// to prevent this unwanted behavior, we will add a debounce check.
		uint32_t now = HAL_GetTick();

		// the way the debounce check works, is we want to prevent unintentional consecutive presses
		// caused by the mechanics of the button itself rather than user intent.
		// to do that, we only accept presses if the button wasn't pressed in the last 50 ms.
		// this will reject all unwanted interrupts caused by the signal bouncing.
		if ((now - last_press) > 50U) {
			enable_blink ^= 1;
		}

		last_press = now;
	}
}
